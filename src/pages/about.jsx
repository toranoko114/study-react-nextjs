import Head from 'next/head'
import styles from 'src/styles/Home.module.css'
import { Footer } from 'src/components/Footer'
import { Header } from 'src/components/Header'
import { Main } from 'src/components/Main'
import { useCounter } from 'src/hooks/useCounter'
import { useInputArray } from 'src/hooks/useInputArray'
import { useBackGroundColerChange } from 'src/hooks/useBackGroundColerChange'

export default function About() {

const {count, isShow, handleClick, handleDisplay} = useCounter();
const {text, array, handleAddArray, handleChange} = useInputArray();
useBackGroundColerChange();

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <Header />

    {isShow ? <h1>{count}</h1> : null}
    <button onClick={handleClick}>ボタン</button>
    <button onClick={handleDisplay}>{isShow ? "カウント非表示" : "カウント表示"}</button>

    <input type='text' value={text} onChange={handleChange}/>
    <button onClick={handleAddArray}>追加</button>
    <ul>
      {array.map(item =>{
        return <li key={Math.random()}>{item}</li>;
      })}
    </ul>

      <Main page="about"/>
      <Footer />
    </div>
  )
}
